#Receba o ano de nascimento e o ano atual. Calcule e mostre a sua idade e quantos anos terá daqui a 17 anos
#declarar
ano1 = int(input("Digite seu ano de nascimento:"))
ano2 = int(input("Digite o ano atual:"))
#início
idade = ano2 - ano1
ano17 = idade + 17
print("Sua idade é", idade, "e daqui 17 anos voce terá", ano17)
#fim